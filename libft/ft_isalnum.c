#include "libft.h"

int	ft_isalnum(int cn)
{
	if ((cn >= 'A' && cn <= 'Z') || (cn >= 'a' && cn <= 'z'))
		return (1);
	else if (cn >= '0' && cn <= '9')
		return (1);
	else
		return (0);
}
